const dayOfWeek = 'friday'

switch (dayOfWeek) {
  case 'sunday' :
    console.log('sunday: first day of the week')
    break
  case 'monday' :
    console.log('monday: second day of the week')
    break
  case 'tuesday' :
    console.log('tuesday: third day of the week')
    break
  case 'wednesday' :
    console.log('wednesday: fourth day of the week')
    break
  case 'thursday' :
    console.log('thursday: fifth day of the week')
    break
  case 'friday' :
    console.log('friday: sixth day of the week')
    break
  case 'saturday' :
    console.log('saturday: seventh day of the week')
    break
  default :
    console.log('invalid day of the week')
    break
}
